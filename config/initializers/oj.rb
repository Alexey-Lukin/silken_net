# = ===================================================================
# ⚡ Oj (Optimized JSON) — Глобальна конфігурація
# = ===================================================================
# Oj замінює стандартний Ruby JSON парсер на надшвидкий C-модуль.
#
# Чому це критично для SilkenNet:
# 1. Blockchain RPC (Polygon, Ethereum, Solana) повертають великі JSON-відповіді,
#    які парсяться у Web3::HttpClient — Oj прискорює це у 3–10×.
# 2. Blueprinter серіалізує API-відповіді для 26 контролерів — Oj пришвидшує
#    генерацію JSON-відповідей для кожного запиту.
# 3. Sidekiq серіалізує/десеріалізує аргументи джоб через JSON — при 16 воркерах
#    та 7 чергах (uplink → low) це помітний виграш у throughput.
# 4. Масштаб: мільйони → мільярди дерев = мільйони телеметричних пакетів/хв,
#    кожен з яких проходить через JSON-парсинг.
#
# Режим :compat забезпечує повну сумісність зі стандартним JSON API:
# - JSON.parse / JSON.generate працюють через Oj під капотом
# - ActiveSupport, Sidekiq, Blueprinter, eth gem — все працює без змін

require "oj"

Oj.mimic_JSON
